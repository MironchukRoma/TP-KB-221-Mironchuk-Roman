
unsorted_list = [
    {'ім’я': 'John', 'оцінка': 4.5},
    {'ім’я': 'Alice', 'оцінка': 4.2},
    {'ім’я': 'Bob', 'оцінка': 4.7},
    {'ім’я': 'Eva', 'оцінка': 4.1}
]

# Вибір параметру сортування (ім’я або оцінка)
sort_option = input("Виберіть параметр сортування (ім’я або оцінка): ").strip()

# Сортування за обраним параметром
sorted_list = sorted(unsorted_list, key=lambda x: x[sort_option])

# Виведення відсортованого списку
print("Відсортований список:")
for item in sorted_list:
    print(f"Ім’я: {item['ім’я']}, Оцінка: {item['оцінка']}")
